export class User {
    name: string;
    age: number;
    email: string;
    profession: string;
    constructor(
        name: string,
        age: number,
        email: string,
        profession: string,
    ) {
        this.name = name;
        this.age = age;
        this.email = email;
        this.profession = profession;
    }
}

    